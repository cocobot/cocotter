fn crc(data: &[u8]) -> u8 {
    let mut c: u8 = 0;
    for &b in data {
        c = CRC_TABLE[(c ^ b) as usize];
    }
    c
}

pub fn encode_packet(
    start_angle_deg: f32,
    end_angle_deg: f32,
    speed_deg_s: u16,
    timestamp_ms: u16,
    distances_mm: [u16; 12],
    intensities: [u8; 12],
) -> [u8; PACKET_SIZE] {
    let mut buf = [0u8; PACKET_SIZE];
    buf[0] = HEADER;
    buf[1] = VER_LEN;
    buf[2..4].copy_from_slice(&speed_deg_s.to_le_bytes());
    let start_centi = (start_angle_deg * 100.0).round().clamp(0.0, 65535.0) as u16;
    buf[4..6].copy_from_slice(&start_centi.to_le_bytes());
    for i in 0..12 {
        let base = 6 + i * 3;
        buf[base..base + 2].copy_from_slice(&distances_mm[i].to_le_bytes());
        buf[base + 2] = intensities[i];
    }
    let end_centi = (end_angle_deg * 100.0).round().clamp(0.0, 65535.0) as u16;
    buf[42..44].copy_from_slice(&end_centi.to_le_bytes());
    buf[44..46].copy_from_slice(&timestamp_ms.to_le_bytes());
    buf[46] = crc(&buf[..46]);
    buf
}

#[cfg(test)]
mod tests {
    use super::*;

    // Same CRC check as in galipeur/src/sensors/ld06.rs.
    fn verify_crc(data: &[u8; PACKET_SIZE]) -> bool {
        let mut c: u8 = 0;
        for &b in &data[..PACKET_SIZE - 1] {
            c = CRC_TABLE[(c ^ b) as usize];
        }
        c == data[PACKET_SIZE - 1]
    }

    #[test]
    fn packet_roundtrip_crc() {
        let distances = [1234u16; 12];
        let intensities = [200u8; 12];
        let pkt = encode_packet(10.0, 40.0, 3000, 1234, distances, intensities);
        assert_eq!(pkt[0], 0x54);
        assert_eq!(pkt[1], 0x2C);
        assert!(verify_crc(&pkt));

        // Decode start/end angle back.
        let start = u16::from_le_bytes([pkt[4], pkt[5]]) as f32 * 0.01;
        let end = u16::from_le_bytes([pkt[42], pkt[43]]) as f32 * 0.01;
        assert!((start - 10.0).abs() < 0.01);
        assert!((end - 40.0).abs() < 0.01);

        // Decode a point.
        let d0 = u16::from_le_bytes([pkt[6], pkt[7]]);
        assert_eq!(d0, 1234);
    }
}
